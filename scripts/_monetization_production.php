function sl_find_post_id_by_slug($slug) {
  $posts = get_posts([
    'name' => $slug,
    'post_type' => 'post',
    'post_status' => 'any',
    'posts_per_page' => 1,
    'fields' => 'ids',
  ]);
  return empty($posts) ? 0 : (int) $posts[0];
}

